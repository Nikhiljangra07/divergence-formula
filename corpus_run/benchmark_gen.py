"""
benchmark_gen.py — generate the OUT-OF-DISTRIBUTION benchmark problem set.

The model trained on literary-strategic sources (RI/JPM/PR/TH). This benchmark tests whether the refraction
skill GENERALIZES to modern, everyday decisions it never saw. 8 categories x N problems each, modern and
non-literary. 'negotiation' is the control (closest to training genre). Output: benchmark/problems.jsonl
with {problem, category}. Local, no GPU. Generated by DSV4 (judge stays independent = Haiku).

  python benchmark_gen.py --per 6
"""
from __future__ import annotations
import argparse, asyncio, json
from pathlib import Path
import httpx
import gen_v3 as g

OUT = g.HERE / "benchmark"; OUT.mkdir(exist_ok=True)

CATEGORIES = {
    "career":         "an individual's CAREER decision (job offers, quitting, pivoting fields, relocation for work, going independent)",
    "business":       "a STARTUP/BUSINESS strategy decision (pivot, hiring/firing, fundraising, pricing, whether to shut down)",
    "relationship":   "an interpersonal/RELATIONSHIP decision (a hard conversation, commitment, a family conflict, a falling-out with a friend)",
    "ethics":         "an ETHICAL dilemma with genuinely competing obligations (loyalty vs honesty, whistleblowing, a promise vs a greater good)",
    "finance":        "a personal FINANCE/risk decision (a large irreversible purchase, debt, a risky investment, supporting family financially)",
    "health_life":    "a HEALTH or LIFE-DIRECTION decision (a lifestyle overhaul, a medical choice with tradeoffs, where to live, a big life reset)",
    "negotiation":    "a COMPETITIVE/NEGOTIATION decision (a salary or deal negotiation, a rivalry, a standoff with a counterparty)",  # control: closest to training
    "creative":       "a CREATIVE/PRODUCT-direction decision (which project to commit to, whether to ship or polish, killing a feature, an artistic risk)",
}

GEN_PROMPT = (
    "Generate {n} DISTINCT, realistic, MODERN decision dilemmas, each of this type:\n{desc}\n\n"
    "Each must be a genuinely HARD choice that admits several defensible strategies (not one obvious answer). "
    "Vary the specific people, stakes, and circumstances so the {n} are clearly different from each other. "
    "Each is 1-2 sentences, concrete and specific, set in the present-day real world (NOT historical, NOT "
    "fictional/literary). No fantasy, no kings, no ancient settings.\n\n"
    "Return STRICT JSON only: {{\"problems\": [\"<dilemma 1>\", \"<dilemma 2>\", ...]}}"
)


async def gen_category(client, cat, desc, n):
    out = await g.call(client, GEN_PROMPT.format(n=n, desc=desc), g.C.MODEL, 2000, 0.9)
    spec = g.parse_json(out)
    if not spec or "problems" not in spec:
        return []
    return [{"problem": str(p).strip(), "category": cat} for p in spec["problems"] if str(p).strip()][:n]


async def main():
    ap = argparse.ArgumentParser(); ap.add_argument("--per", type=int, default=6)
    args = ap.parse_args()
    print(f"benchmark_gen — {len(CATEGORIES)} categories x {args.per} = {len(CATEGORIES)*args.per} problems | cap ${g.HARD_USD_CAP}", flush=True)
    async with httpx.AsyncClient() as client:
        results = await asyncio.gather(*[gen_category(client, c, d, args.per) for c, d in CATEGORIES.items()])
    probs = [p for sub in results for p in sub]
    (OUT / "problems.jsonl").write_text("\n".join(json.dumps(p) for p in probs) + "\n")
    from collections import Counter
    print(f"generated {len(probs)} | by category: {dict(Counter(p['category'] for p in probs))} | spend ${g.LED.spent:.3f}", flush=True)


if __name__ == "__main__":
    asyncio.run(main())
